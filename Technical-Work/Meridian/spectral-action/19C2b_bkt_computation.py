# The BKT modifies the effective gauge coupling:
#   1/g_i^2(eff) = a_i * f0 + delta_i
# where a_i * f0 is the universal spectral action contribution
# and delta_i is the BKT.
#
# In terms of alpha^{-1}:
#   alpha_i^{-1}(eff) = alpha_i^{-1}(spectral) + 4*pi * delta_i
# where the 4pi comes from alpha = g^2/(4*pi)
#
# The BKT from one-loop quantum corrections:
#   delta_i = (b_i / 16*pi^2) * L
# where L is the relevant logarithm.
#
# The shift in alpha^{-1} is:
#   Delta(alpha_i^{-1}) = (b_i / 16*pi^2) * L * 4*pi = (b_i / (4*pi)) * L
#
# Wait — let's be more careful. The BKT is:
#   delta(1/g_i^2) = (b_i / (16*pi^2)) * L
# And alpha_i^{-1} = 4*pi / g_i^2, so:
#   delta(alpha_i^{-1}) = 4*pi * delta(1/g_i^2) = (b_i / (4*pi)) * L
#
# This is equivalent to the standard one-loop running:
#   alpha_i^{-1}(mu1) - alpha_i^{-1}(mu2) = (b_i / (2*pi)) * ln(mu1/mu2)
# with L = 2*ln(Lambda/mu) = ln(Lambda^2/mu^2).

# Actually, the BKT contribution to 1/g_i^2 is:
#   delta(1/g_i^2)_BKT = (b_i / (16 pi^2)) * ln(Lambda^2 / mu^2)
# The corresponding shift in alpha_i^{-1}:
#   delta(alpha_i^{-1}) = 4*pi * delta(1/g_i^2) = b_i * ln(Lambda^2/mu^2) / (4*pi)
#                       = b_i * 2*ln(Lambda/mu) / (4*pi)
#                       = b_i * ln(Lambda/mu) / (2*pi)
# which is EXACTLY the one-loop running formula. Good — consistent.

def bkt_shift(b_i, log_ratio):
    """
    BKT shift in alpha_i^{-1}.

    delta(alpha_i^{-1}) = b_i/(2*pi) * log_ratio

    where log_ratio = ln(Lambda/mu) is the relevant logarithm.
    """
    return b_i / (2 * np.pi) * log_ratio


