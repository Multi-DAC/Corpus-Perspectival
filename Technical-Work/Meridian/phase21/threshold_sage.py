# For O(n) on P^1, the non-zero eigenvalues of the Laplacian are:
# lambda_k = k(k+n+1) with multiplicities depending on the bundle
#
# The zeta function is:
# zeta_n(s) = sum_{k=1}^infty [k(k+|n|+1)]^{-s}  (for n >= 0)
#
# This can be decomposed using partial fractions:
# 1/[k(k+m)]^s = (1/m)^s * sum involving Hurwitz zeta
#
# For s -> 0: zeta'_n(0) = log(m!) - m*log(m) + ... (Stirling-like)

from sage.all import *

# Compute zeta'(0) for the spectral zeta of O(n) on P^1
# Using the relation to Barnes zeta function
def spectral_zeta_prime_P1(n, num_terms=10000):
    """
    Compute zeta'(0) for the scalar Laplacian on P^1 = S^2
    twisted by O(n), using zeta regularization.

    Eigenvalues: lambda_k = k(k + |n| + 1) for k >= 1
    (ignoring zero modes)

    Uses the partial fraction decomposition:
    sum_k log(k(k+m)) = sum_k [log(k) + log(k+m)]
                       = zeta'_H(0, 1) + zeta'_H(0, 1+m)
    where zeta_H is the Hurwitz zeta.

    Hurwitz zeta derivative: zeta'_H(0, a) = log(Gamma(a)) - log(2*pi)/2
    """
    m = abs(n) + 1
    # zeta'(0) for sum_k log(k(k+m)):
    # = -[zeta'_H(0,1) + zeta'_H(0, 1+m)]  (note the sign convention)
    # = -[log(Gamma(1)) - log(2*pi)/2 + log(Gamma(1+m)) - log(2*pi)/2]
    # = -[0 - log(2*pi)/2 + log(m!) - log(2*pi)/2]
    # = log(2*pi) - log(m!)

    # More carefully: the spectral zeta function is
    # Z(s) = sum_{k=1}^inf [k(k+m)]^{-s}
    # Z'(0) = -sum_{k=1}^inf log[k(k+m)]
    # = -sum_{k=1}^inf [log(k) + log(k+m)]

    val = RR(0)
    # Numerical computation for verification
    for k in range(1, num_terms + 1):
        val -= RR(log(k * (k + m)))

    # Regularized value using Hurwitz zeta:
    # sum_{k=1}^inf log(k) = -zeta'_R(0) = log(2*pi)/2  (Riemann zeta)
    # sum_{k=1}^inf log(k+m) = -zeta'_H(0, 1+m) = -log(Gamma(1+m)) + log(2*pi)/2

    reg_val = -RR(log(2*RR(pi))/2) - (-RR(log(gamma(1+m))) + RR(log(2*RR(pi))/2))
    reg_val = RR(log(gamma(1+m))) - RR(log(2*RR(pi)))

    return reg_val

print("\nRegularized spectral determinants log det(Delta) for O(n) on P^1:")
print(f"{'n':>4} {'|n|+1':>6} {'log det (reg)':>15} {'Gamma factor':>15}")
