def theta1(z, tau, N=_sage_const_300 ):
    """Jacobi theta_1(z|tau) = 2*sum_{n>=0} (-1)^n q^{(n+1/2)^2/2} sin((2n+1)*pi*z)"""
    q = C(exp(_sage_const_2 *pi*I*tau))
    result = C(_sage_const_0 )
    for n in range(N):
        sgn = (-_sage_const_1 )**n
        qpow = (n + R(_sage_const_1 )/R(_sage_const_2 ))**_sage_const_2  / R(_sage_const_2 )
        sarg = (_sage_const_2 *n+_sage_const_1 ) * pi_val * R(z)
        result += C(sgn) * q**qpow * C(sin(sarg))
    return _sage_const_2  * result

def theta2(z, tau, N=_sage_const_300 ):
    """Jacobi theta_2(z|tau) = 2*sum_{n>=0} q^{(n+1/2)^2/2} cos((2n+1)*pi*z)"""
    q = C(exp(_sage_const_2 *pi*I*tau))
    result = C(_sage_const_0 )
    for n in range(N):
        qpow = (n + R(_sage_const_1 )/R(_sage_const_2 ))**_sage_const_2  / R(_sage_const_2 )
        carg = (_sage_const_2 *n+_sage_const_1 ) * pi_val * R(z)
        result += q**qpow * C(cos(carg))
    return _sage_const_2  * result

def theta3(z, tau, N=_sage_const_300 ):
    """Jacobi theta_3(z|tau) = 1 + 2*sum_{n>=1} q^{n^2/2} cos(2*n*pi*z)"""
    q = C(exp(_sage_const_2 *pi*I*tau))
